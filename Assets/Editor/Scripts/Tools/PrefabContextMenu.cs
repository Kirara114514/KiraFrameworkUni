#region DeferredBinder
[InitializeOnLoad]
public static class DeferredBinder
{
    private const string PREF_KEY = "PrefabAutoBindInfo";

    static DeferredBinder()
    {
        AssemblyReloadEvents.afterAssemblyReload += OnScriptsReloaded;
    }

    public static void ScheduleBinding(GameObject prefab, string scriptPath, UIInfo info)
    {
        var data = new DeferredData
        {
            prefabPath = AssetDatabase.GetAssetPath(prefab),
            scriptPath = scriptPath,
            entries = info.entries
        };

        var json = JsonUtility.ToJson(data);
        EditorPrefs.SetString(PREF_KEY, json);
        Debug.Log($"[DeferredBinder] 已记录延迟绑定任务：{data.prefabPath}");
    }

    private static void OnScriptsReloaded()
    {
        if (!EditorPrefs.HasKey(PREF_KEY)) return;

        string json = EditorPrefs.GetString(PREF_KEY);
        EditorPrefs.DeleteKey(PREF_KEY);
        Debug.Log("[DeferredBinder] 检测到脚本重新加载，开始执行延迟绑定。");

        var data = JsonUtility.FromJson<DeferredData>(json);
        if (data == null)
        {
            Debug.LogWarning("[DeferredBinder] 反序列化延迟绑定数据失败。");
            return;
        }

        GameObject prefabAsset = AssetDatabase.LoadAssetAtPath<GameObject>(data.prefabPath);
        if (prefabAsset == null)
        {
            Debug.LogWarning($"[DeferredBinder] 找不到 Prefab：{data.prefabPath}");
            return;
        }

        string className = Path.GetFileNameWithoutExtension(data.scriptPath);
        System.Type scriptType = GetTypeByName(className);
        if (scriptType == null)
        {
            Debug.LogWarning($"[DeferredBinder] 找不到类型 {className}，绑定失败。");
            return;
        }

        Debug.Log($"[DeferredBinder] 成功找到脚本类型：{className}");
        GameObject prefabInstance = PrefabUtility.LoadPrefabContents(data.prefabPath);
        var comp = prefabInstance.GetComponent(scriptType);
        if (comp == null)
        {
            comp = prefabInstance.AddComponent(scriptType);
            Debug.Log($"[DeferredBinder] 在 Prefab 上添加脚本组件：{className}");
        }
        else
        {
            Debug.Log($"[DeferredBinder] Prefab 已存在脚本组件：{className}");
        }

        var so = new SerializedObject(comp);
        int bindCount = 0;
        foreach (var e in data.entries)
        {
            var prop = so.FindProperty(e.varName);
            if (prop != null)
            {
                prop.objectReferenceValue = e.target;
                bindCount++;
                Debug.Log($"[DeferredBinder] 绑定字段：{e.varName} ← {e.target}");
            }
        }
        so.ApplyModifiedProperties();

        PrefabUtility.SaveAsPrefabAsset(prefabInstance, data.prefabPath);
        PrefabUtility.UnloadPrefabContents(prefabInstance);

        Debug.Log($" 延迟绑定完成：{className} ({bindCount} 项)。");
    }

    private static System.Type GetTypeByName(string className)
    {
        foreach (var asm in System.AppDomain.CurrentDomain.GetAssemblies())
        {
            var type = asm.GetType(className);
            if (type != null)
                return type;
        }
        return null;
    }

    [System.Serializable]
    private class DeferredData
    {
        public string prefabPath;
        public string scriptPath;
        public List<UIEntry> entries;
    }
}
#endregion


#region DataTypes
[System.Serializable]
public class UIInfo
{
    public List<UIEntry> entries = new List<UIEntry>();
    public HashSet<string> usings = new HashSet<string>();
}

[System.Serializable]
public class UIEntry
{
    public string varName;
    public string typeName;
    public Object target;
}
#endregion
